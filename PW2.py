import math

# Зчитуємо кількість чисел p (хоча для самого обчислення в Python вона не є обов'язковою)
p = int(input())

# Зчитуємо рядок чисел, розділених пробілом, і перетворюємо їх на список цілих чисел
numbers = list(map(int, input().split()))

# Знаходимо НСК для всього списку чисел
# Оператор * розпаковує список у аргументи функції math.lcm
result = math.lcm(*numbers)

# Виводимо результат
print(result)